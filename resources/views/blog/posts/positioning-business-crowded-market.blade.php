                <p class="text-gray-700 text-sm">Unique way of delivering your service</p>
            </div>
            <div class="bg-white rounded-lg p-4">
                <i class="fas fa-user text-green-500 text-xl mb-2"></i>
                <h6 class="font-semibold text-gray-900 mb-1">Personality</h6>
                <p class="text-gray-700 text-sm">Your brand voice and character</p>
            </div>
            <div class="bg-white rounded-lg p-4">
                <i class="fas fa-graduation-cap text-green-500 text-xl mb-2"></i>
                <h6 class="font-semibold text-gray-900 mb-1">Expertise</h6>
                <p class="text-gray-700 text-sm">Specialized knowledge or experience</p>
            </div>
