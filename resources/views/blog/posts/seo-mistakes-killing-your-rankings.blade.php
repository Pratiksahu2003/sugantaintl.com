        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">4. What is the biggest SEO mistake to avoid?</h4>
            <p class="text-gray-700">
                Ignoring your audience. SEO is not just about search engines — it's about people. If your content does not solve a real problem or meet a real need, rankings will suffer.
            </p>
        </div>
        
