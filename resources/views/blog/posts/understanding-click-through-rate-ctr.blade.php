    <h3 class="text-2xl font-bold text-gray-900 mb-4">FAQs</h3>
    
    <div class="space-y-6 mb-6">
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">1. What exactly does CTR mean in digital marketing?</h4>
            <p class="text-gray-700">
                CTR (Click-Through Rate) shows the percentage of people who clicked on your link, ad, or post after seeing it. 
                It helps measure how engaging and relevant your content is to your audience. A higher CTR usually means your 
                message is connecting well.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">2. Why is CTR so important?</h4>
            <p class="text-gray-700">
                CTR matters because it directly reflects audience interest. A good CTR means your ad copy, visuals, or headlines 
                are doing their job – grabbing attention and driving action. It also affects SEO and ad performance, helping you 
                get better visibility and lower ad costs.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">3. What is considered a "good" CTR?</h4>
            <p class="text-gray-700 mb-3">There's no one-size-fits-all answer, but here's a general idea:</p>
            <ul class="text-gray-700 space-y-1 ml-4">
                <li>• <strong>Google Search Ads:</strong> 3%–5%</li>
                <li>• <strong>Display Ads:</strong> 0.5%–1%</li>
                <li>• <strong>Email Campaigns:</strong> 2%–5%</li>
            </ul>
            <p class="text-gray-700 mt-3">
                The goal is to track your own average CTR and continuously improve over time.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">4. How can I increase my CTR naturally?</h4>
            <p class="text-gray-700 mb-3">You can improve your CTR by:</p>
            <ul class="text-gray-700 space-y-1 ml-4">
                <li>• Writing strong, emotional headlines</li>
                <li>• Using clear and action-oriented CTAs (like "Get Started" or "See How It Works")</li>
                <li>• Adding eye-catching visuals</li>
                <li>• Keeping your content relevant and mobile-friendly</li>
                <li>• Testing different versions of your creatives and ad copies</li>
            </ul>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">5. Does CTR affect SEO rankings?</h4>
            <p class="text-gray-700">
                Yes, indirectly. When users click on your page more often, it signals to Google that your content is relevant. 
                This can improve your search ranking and visibility over time – which means better organic traffic and performance.
            </p>
