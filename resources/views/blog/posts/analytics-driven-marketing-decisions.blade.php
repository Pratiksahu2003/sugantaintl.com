    <div class="space-y-6 mb-6">
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">1. What is marketing analytics?</h4>
            <p class="text-gray-700">
                Marketing analytics is the process of collecting, measuring, and analyzing data from marketing activities to understand 
                performance and make smarter decisions. It helps businesses know what's working, what's not, and where to improve.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">2. Why is data-driven marketing important?</h4>
            <p class="text-gray-700">
                Data-driven marketing ensures every decision is based on facts, not assumptions. It helps marketers target the right 
                audience, optimize spending, and increase ROI by focusing on strategies that deliver measurable results.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">3. What are the key metrics to track in marketing analytics?</h4>
            <p class="text-gray-700">
                Some important metrics include click-through rate (CTR), conversion rate, cost per acquisition (CPA), return on 
                investment (ROI), website traffic, and engagement rate. These metrics show how effectively your campaigns are performing.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">4. How can small businesses use analytics effectively?</h4>
            <p class="text-gray-700">
                Even with limited budgets, small businesses can use free tools like Google Analytics, Meta (Facebook) Insights, 
                or Mailchimp reports to understand customer behavior, track conversions, and improve their marketing efforts gradually.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">5. What are some of the best marketing analytics tools?</h4>
            <p class="text-gray-700">
                Popular tools include Google Analytics, HubSpot, SEMrush, Ahrefs, Hootsuite Analytics, and Tableau. Each offers 
                different insights – from website performance to SEO, social media, and customer behavior.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">6. How often should marketers review their analytics data?</h4>
            <p class="text-gray-700">
                Ideally, analytics should be reviewed weekly or monthly, depending on your campaign size. Regular reviews help 
                identify trends early, fix underperforming campaigns, and make real-time adjustments for better outcomes.
            </p>
