                    <code class="text-purple-600 font-mono text-lg">
                        ROI = (Revenue from Campaign – Campaign Cost) ÷ Campaign Cost × 100
                    </code>
                </div>
            </div>
            <p class="text-purple-800">
                This ensures you're investing in strategies that truly deliver results, rather than wasting budget.
