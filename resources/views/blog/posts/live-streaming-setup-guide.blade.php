    <div class="bg-gray-50 rounded-lg p-6 mb-6">
        <h4 class="text-lg font-semibold text-gray-900 mb-3">Audio Configuration</h4>
        <ul class="space-y-2">
            <li><strong>Microphone Placement:</strong> Position microphones for optimal sound capture</li>
            <li><strong>Audio Levels:</strong> Set appropriate input levels to avoid distortion</li>
            <li><strong>Noise Reduction:</strong> Use noise gates and filters to reduce background noise</li>
            <li><strong>Monitoring:</strong> Use headphones to monitor audio quality in real-time</li>
            <li><strong>Backup Audio:</strong> Have secondary audio sources ready</li>
            <li><strong>Audio Sync:</strong> Ensure audio and video are properly synchronized</li>
