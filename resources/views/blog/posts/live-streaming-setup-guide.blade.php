    <h3 class="text-2xl font-bold text-gray-900 mb-4">Stream Quality Optimization</h3>
    
    <div class="grid grid-cols-1 md:grid-cols-2 gap-6 mb-6">
        <div class="bg-green-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-green-900 mb-3">Video Settings</h4>
            <ul class="text-green-800 space-y-2">
                <li>• Resolution: 720p or 1080p</li>
                <li>• Frame Rate: 30fps or 60fps</li>
                <li>• Bitrate: 2500-6000 kbps</li>
                <li>• Encoder: Hardware (NVENC) or Software (x264)</li>
                <li>• Keyframe Interval: 2 seconds</li>
                <li>• Color Space: Rec. 709</li>
            </ul>
        </div>
        
        <div class="bg-blue-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-blue-900 mb-3">Audio Settings</h4>
            <ul class="text-blue-800 space-y-2">
                <li>• Sample Rate: 44.1 kHz or 48 kHz</li>
                <li>• Bit Depth: 16-bit or 24-bit</li>
                <li>• Channels: Mono or Stereo</li>
                <li>• Bitrate: 128-320 kbps</li>
                <li>• Codec: AAC or MP3</li>
                <li>• Normalization: Enable for consistent levels</li>
            </ul>
        </div>
