    <div class="space-y-6 mb-6">
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">1. What is keyword research in simple terms?</h4>
            <p class="text-gray-700">
                Keyword research means finding out what words or phrases people type into search engines when they're looking for 
                something related to your business. It helps you create content that matches what your audience is already searching 
                for – making it easier for them to find you online.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">2. Why is keyword research still important in 2025?</h4>
            <p class="text-gray-700">
                Even with AI, voice search, and smart algorithms, keywords remain the backbone of SEO. Search engines still rely on 
                them to understand what your content is about. The better your keyword strategy, the more likely you are to appear 
                in front of the right audience and drive organic traffic.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">3. What's the difference between short-tail and long-tail keywords?</h4>
            <div class="grid grid-cols-1 md:grid-cols-2 gap-4">
                <div>
                    <h5 class="font-semibold text-gray-900 mb-2">Short-tail keywords:</h5>
