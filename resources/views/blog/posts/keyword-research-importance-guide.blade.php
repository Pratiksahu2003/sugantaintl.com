                        General phrases like "digital marketing" – they have high search volume but also high competition.
                    </p>
                </div>
                <div>
                    <h5 class="font-semibold text-gray-900 mb-2">Long-tail keywords:</h5>
                    <p class="text-gray-700 text-sm">
                        More specific phrases like "affordable digital marketing agency for startups" – they attract fewer searches 
                        but convert better because they target users with clear intent.
                    </p>
                </div>
            </div>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">4. How do I know which keywords are right for my business?</h4>
            <p class="text-gray-700">
                Start by understanding your audience and what they're searching for. Then use keyword tools like Google Keyword Planner, 
                SEMrush, or Ahrefs to check search volume, difficulty, and intent. Choose keywords that balance relevance, search volume, 
                and competition.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">5. How many keywords should I target per page or blog?</h4>
