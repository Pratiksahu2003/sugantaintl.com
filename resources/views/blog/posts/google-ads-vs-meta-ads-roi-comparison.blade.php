    <div class="space-y-6 mb-6">
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">1. What's the main difference between Google Ads and Meta Ads?</h4>
            <p class="text-gray-700">
                The biggest difference lies in user intent. Google Ads targets people actively searching for products or services – 
                meaning high intent. Meta Ads (Facebook & Instagram) targets users based on interests and behaviors – meaning 
                discovery-based. Both can deliver great ROI, depending on your goals.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">2. Which platform gives better ROI for small businesses?</h4>
            <p class="text-gray-700">
                For small businesses, Meta Ads often provide a better ROI initially because they have lower costs per click and allow 
                you to build brand awareness affordably. However, if you're selling a service or product people are already searching 
                for, Google Ads can yield faster conversions.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">3. Is it better to use both Google Ads and Meta Ads together?</h4>
            <p class="text-gray-700">
                Yes, absolutely. Many successful brands use both platforms strategically: Meta Ads for awareness and retargeting, 
                Google Ads for capturing high-intent leads ready to buy. Together, they cover the full customer journey – from 
                discovery to decision.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
