    <h3 class="text-2xl font-bold text-gray-900 mb-4">FAQs</h3>
    
    <div class="space-y-6 mb-6">
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">1. What are digital marketing KPIs?</h4>
            <p class="text-gray-700">
                Digital marketing KPIs (Key Performance Indicators) are measurable values that help you track the effectiveness 
                of your marketing campaigns. They show how well your business is achieving specific goals, such as increasing 
                website traffic, generating leads, or boosting sales.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">2. Why are KPIs important in digital marketing?</h4>
            <p class="text-gray-700">
                KPIs help marketers make data-driven decisions. By tracking metrics like conversion rate, ROI, and engagement, 
                businesses can understand what's working, eliminate ineffective strategies, and invest more in what brings real results.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">3. How do I choose the right KPIs for my business?</h4>
            <p class="text-gray-700 mb-3">Choose KPIs that align with your business goals:</p>
            <ul class="text-gray-700 space-y-1 ml-4">
                <li>• <strong>For brand awareness:</strong> focus on impressions, reach, and traffic</li>
                <li>• <strong>For lead generation:</strong> track conversions and cost per acquisition (CPA)</li>
                <li>• <strong>For retention:</strong> monitor customer lifetime value (CLV) and engagement</li>
            </ul>
            <p class="text-gray-700 mt-3">
                Your KPIs should reflect your unique objectives and industry type.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">4. How often should I measure and analyze KPIs?</h4>
            <p class="text-gray-700">
                It depends on your campaign. For paid ads or social media, weekly or even daily tracking is helpful. For broader 
                metrics like ROI or CLV, monthly or quarterly analysis gives a clearer long-term picture. Consistency is key to 
                spotting trends early.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">5. What are some common tools to track digital marketing KPIs?</h4>
            <p class="text-gray-700 mb-3">Some widely used tools include:</p>
            <ul class="text-gray-700 space-y-1 ml-4">
                <li>• <strong>Google Analytics</strong> (website traffic, conversions)</li>
                <li>• <strong>Meta Business Suite</strong> (social media insights)</li>
                <li>• <strong>HubSpot</strong> (lead tracking and CRM analytics)</li>
                <li>• <strong>Mailchimp</strong> (email performance)</li>
                <li>• <strong>SEMrush or Ahrefs</strong> (SEO KPIs)</li>
            </ul>
            <p class="text-gray-700 mt-3">
                These tools provide comprehensive dashboards for tracking campaign performance.
            </p>
        </div>
        
        <div class="bg-gray-50 rounded-lg p-6">
            <h4 class="text-lg font-semibold text-gray-900 mb-2">6. What's the difference between metrics and KPIs?</h4>
            <p class="text-gray-700 mb-3">
                All KPIs are metrics, but not all metrics are KPIs. Metrics are general data points (like page views or likes), 
                while KPIs are the most critical metrics that directly measure progress toward business goals – for example, 
                conversion rate or ROI.
            </p>
